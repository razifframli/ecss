    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
         int episodeRow = 0;

         String[] patInfo={};
         PatientBean1 patDetails = new PatientBean1();
         try {
             patInfo = patDetails.getPatientUsingPMI(pmiNo);
         } catch (ClassNotFoundException ex) {
             System.out.println(ex);
         } catch (SQLException ex) {
             System.out.println(ex);
         }

         lblNotePMI.setText(patInfo[0]);
         lblNoteName.setText(patInfo[1]);
         lblNoteNewIC.setText(patInfo[2]);
         lblNoteRace.setText(patInfo[24]);
         lblNoteBloodGrp.setText(patInfo[25]);
         lblNoteAllergy.setText(patInfo[26]);

         Connection conn = AccessDB.getConnInstance();
       try
       {
           PreparedStatement ps1 = conn.prepareStatement("SELECT EPISODE_DATE FROM EPISODE WHERE PMI_NO = ?");
           ps1.setString(1, patInfo[0]);
           ResultSet results1 = ps1.executeQuery();

           PreparedStatement ps2 = conn.prepareStatement("SELECT COUNT(*) FROM EPISODE");
           ResultSet result2 = ps2.executeQuery();
           while(result2.next())
               episodeRow = result2.getInt(1);

           if(cmbEpisode.getItemCount() <= episodeRow)
           {
                while(results1.next())
                    cmbEpisode.addItem(results1.getString("EPISODE_DATE"));
           }
       }
       catch(Exception ex)
       {
           System.out.print(ex);
       }
    }//GEN-LAST:event_formWindowActivated

    private void cmbEpisodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbEpisodeActionPerformed
        String eDate = cmbEpisode.getSelectedItem().toString();
        Connection conn = AccessDB.getConnInstance();

       try
       {
           PreparedStatement ps = conn.prepareStatement("SELECT * FROM EPISODE where EPISODE_DATE = ? ");
           ps.setString(1,eDate);
           ResultSet results = ps.executeQuery();

            while(results.next())
            {
               String temperature = String.valueOf(results.getString("TEMPERATURE"));
               String pulse = results.getString("PULSE");
               String resRate = results.getString("RESPIRATORY_RATE");
               String bloodPressure = results.getString("BLOOD_PRESSURE");
               String diagnosis = results.getString("DIAGNOSIS");
               String complaints = results.getString("COMPLAINTS");
               String test = results.getString("TEST");

               lblTemperature.setText(temperature);
               lblPulse.setText(pulse);
               lblResRate.setText(resRate);
               lblBloodPressure.setText(bloodPressure);
               lblDiagnosis.setText(diagnosis);
               lblComplaints.setText(complaints);
               lblTest.setText(test);
            }
       }
       catch(Exception ex){
           System.out.print(ex);}
    }//GEN-LAST:event_cmbEpisodeActionPerformed

