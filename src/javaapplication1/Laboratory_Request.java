    public void accept_button_lio() {
        String lio[] = new String[3];
        for (int i = 0; i < 3; i++) {
            lio[i] = "";
        }
        
        for (int i = 0; i < cons.sizeLRPK; i++) {
            if (cons.cbxPK[i].isSelected()) {
                lio[0] += cons.cbxPK[i].getText() + ", ";
                set_accept_button_lio("Chemist Patology", cons.cbxPK[i].getText());
            }
        }
        for (int i = 0; i < cons.sizeLRKL; i++) {
            if (cons.cbxKL[i].isSelected()) {
                lio[1] += cons.cbxKL[i].getText() + ", ";
                set_accept_button_lio("Clinical", cons.cbxKL[i].getText());
            }
        }
        for (int i = 0; i < cons.sizeLRHM; i++) {
            if (cons.cbxHM[i].isSelected()) {
                lio[2] += cons.cbxHM[i].getText() + ", ";
                set_accept_button_lio("Hematology", cons.cbxHM[i].getText());
            }
        }
    }
    
