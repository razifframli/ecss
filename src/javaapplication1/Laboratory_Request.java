    public void setLabRequest() {
        ArrayList<String> lrPK = new ArrayList<String>();
        ArrayList<String> lrKL = new ArrayList<String>();
        ArrayList<String> lrHM = new ArrayList<String>();
        lrPK = DBConnection.getLabRequest("Patologi Kimia");
        lrKL = DBConnection.getLabRequest("Klinikal");
        lrHM = DBConnection.getLabRequest("Hematologi");
        pnl_patologi_kimia2.setLayout(new GridLayout(cons.sizeLR, 1));
        cons.sizeLRPK = lrPK.size();
        for (int i = 0; i < lrPK.size(); i++) {
            cons.cbxPK[i] = new JCheckBox();
            cons.cbxPK[i].setText(lrPK.get(i));
            pnl_patologi_kimia2.add(cons.cbxPK[i]);
        }
        pnl_klinikal1.setLayout(new GridLayout(cons.sizeLR, 1));
        cons.sizeLRKL = lrKL.size();
        for (int i = 0; i < lrKL.size(); i++) {
            cons.cbxKL[i] = new JCheckBox();
            cons.cbxKL[i].setText(lrKL.get(i));
            pnl_klinikal1.add(cons.cbxKL[i]);
        }
        pnl_hematologi1.setLayout(new GridLayout(cons.sizeLR, 1));
        cons.sizeLRHM = lrHM.size();
        for (int i = 0; i < lrHM.size(); i++) {
            cons.cbxHM[i] = new JCheckBox();
            cons.cbxHM[i].setText(lrPK.get(i));
            pnl_hematologi1.add(cons.cbxHM[i]);
        }
    }
    
