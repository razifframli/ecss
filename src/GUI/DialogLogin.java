    private void jLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginActionPerformed
        // TODO add your handling code here:

        String username = jUsername.getText();
        String password = jPasswordField1.getText();

        if(username.equals("") || password.equals("")) {

            String message = "Please enter your username and password";
            JOptionPane.showMessageDialog(null, message, "Warning", JOptionPane.WARNING_MESSAGE);

        }else if(username.equals("staff") && password.equals("staff"))
        {
            DialogStaffPage staff;
            try {
                staff = new DialogStaffPage();
                staff.show();
                this.dispose();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(DialogLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(DialogLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else{
            try {
                String id = new Member().memberlogin1(this.jUsername.getText(), this.jPasswordField1.getText());
                if ( id != "false") {

                    this.dispose();
                    //this.getParent().hide();
                    DialogPatientPage page = new DialogPatientPage(id);
                    page.setVisible(true);

                } else {
                    String message = "Invalid username and password!";
                    JOptionPane.showMessageDialog(null, message, "Error", JOptionPane.ERROR_MESSAGE);
                    jUsername.setText("");
                    jPasswordField1.setText("");
                    jUsername.getCursor();
                }
            } catch (SQLException ex) {
                Logger.getLogger(DialogLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(DialogLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}//GEN-LAST:event_jLoginActionPerformed

