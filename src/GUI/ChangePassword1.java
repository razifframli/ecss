    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        char currpwd[] = txt_currpwd.getPassword();
        char newpwd[] = txt_newpwd.getPassword();
        char confpwd[] = txt_confirmpwd.getPassword();
        String userid = Session.getUser_id();
        
        String pwd = "";
        String pwd_curr = "";
        String pwd_new = "";
        
        for (int i = 0; i < confpwd.length; i++) {
            pwd += confpwd[i];
        }
        for (int i = 0; i < currpwd.length; i++) {
            pwd_curr += currpwd[i];
        }
        for (int i = 0; i < newpwd.length; i++) {
            pwd_new += newpwd[i];
        }
        
        if (!pwd_curr.equals(Session.getPassword())) {
            J.o("Invalid Password", "Wrong Current Password!", 0);
            return;
        }
        
        if (newpwd.length <= 0) {
            J.o("Blank Password", "Password cannot be blank!", 0);
            return;
        }
        
        if (!pwd_new.equals(pwd)) {
            J.o("Password Not Match", "New Password and Confirmation Password not match!", 0);
            return;
        }
        
        boolean stat = DBConnection.changePassword(Session.getUser_id(), pwd);
        boolean stat2 = false;
        
        try {
//            LongRunProcess.check_network2();
//            if (Session.getPrev_stat()) { //Online 
//                ArrayList<String> listOnline = Func.readXML("online");
//                Registry myRegistry = LocateRegistry.getRegistry(listOnline.get(0), 1099);
//                Message impl = (Message) myRegistry.lookup("myMessage");
//                impl.changePassword(Session.getUser_id(), pwd);
//            }
            stat2 = DBConnection.getImpl().changePassword(Session.getUser_id(), pwd);
        } catch (Exception e) {
            stat2 = false;
            J.o("Network Error", "Warning! Password don't changed ..\nError: "+e.getMessage(), 0);
        }
        
        if (stat && stat2) {
            J.o("Change Password", "Change Password Success ..", 1);
            Account1.txt_pwd.setText(Func.getPwd1(pwd));
            Session.setPassword(pwd);
        } else {
            J.o("Change Password", "Change Password Failed!!", 0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

